package models;

import java.time.LocalDateTime;

/**
 *
 * @author 42labinfo
 */
public class Ingredient extends EntityId{
    public Ingredient(){}
    
    private String name;
    private float price;
    private float weight;
    private int quantity;
    private String type;
    
   

    public Ingredient(String name, float price, float weight, int quantity, String type, int id, LocalDateTime creation, LocalDateTime update, String status) {
        super(id, creation, update, status);
        this.name = name;
        this.price = price;
        this.weight = weight;
        this.quantity = quantity;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    
   
   
    
    
    
    
    
    
    
    
    
    
    
    
   
}
